CREATE DATABASE INOX;
USE INOX;

CREATE TABLE PRODUTOS(

	CODIGO INTEGER UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    IMG VARCHAR(120) NOT NULL,
    NOME VARCHAR(80) NOT NULL

);

CREATE TABLE PEDIDOS(

	CODIGO INTEGER UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    NOME VARCHAR(80),
    DT_INICIO DATE NOT NULL,
    DT_FIM DATE,
	LARGURA DOUBLE(9,5) NOT NULL,
    COMPRIMENTO DOUBLE(9,5) NOT NULL,
    PRODUTOS_CODIGO INTEGER NOT NULL,
    
    FOREIGN KEY(PRODUTOS_CODIGO) REFERENCES PRODUTOS(CODIGO)

);

